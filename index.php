					<p>Ubicado en Avenida Rosario 1977, consta de tres grandes espacios (Aprendizaje, Cine y Entretenimiento) que ofrecen cursos y talleres, videoconferencias, jornadas de debate y espacios de recreación.</p>

					<p>Bajo el lema “Conectados Somos Más”, el intendente Diego León Barreto encabezó hoy el acto de inauguración del primer Punto Digital de la ciudad, un espacio público destinado a brindar a todos los vecinos capacitación, conectividad y acceso a las nuevas tecnologías de la información y la comunicación.</p>

					<p>“Es un verdadero orgullo para nosotros haber gestionado el primer Punto Digital para la ciudad y poder estar abriendo las puertas de este centro público de formación tecnológica y entretenimiento que desde hoy queda abierto para toda la comunidad funense”, destacó León Barreto.</p> 

					<p>El Punto Digital Funes, ubicado en Avenida Rosario 1977, consta de tres grandes espacios (Aprendizaje, Cine y Entretenimiento) que ofrecen diferentes cursos y talleres tanto presenciales como online, además de videoconferencias, jornadas de debate y espacios de recreación.</p>

					<p>Dentro de su programa de Aprendizaje cuenta con aulas equipadas con conexión a internet para brindar cursos y talleres sobre alfabetización digital, formación laboral y sensibilización sobre diferentes problemáticas socioculturales, entre otros temas.</p>

					<p>También se dictarán capacitaciones sobre arte digital, utilizando las últimas tecnologías para la elaboración de efectos visuales, producción musical o historietas, entre otras creaciones artísticas.</p>

					<p>Por su parte, la sala de Cine pretende ser un lugar de encuentro comunitario, donde se proyectarán películas y otros materiales audiovisuales que dispararán el debate y la reflexión.</p>

					<p>Por último, el área de Entretenimiento se encuentra provista de consolas de videojuegos de última generación, destinadas a la recreación y el esparcimiento de niños, jóvenes y adultos.</p>

					<p>Cabe remarcar que los Puntos Digitales forman parte de las políticas de inclusión tecnológica desarrolladas por el Ministerio de Modernización, a través del Plan País Digital</p> 
